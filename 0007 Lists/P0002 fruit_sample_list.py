fruits = [
    'apple',
    'orange',
    'peer',
    'strawberry'
]

print(fruits)

# You can also print specific items
print(fruits[1:3]) # ['orange', 'peer']
print(fruits[:3]) # ['apple', 'orange', 'peer']
print(fruits[::2]) # ['apple', 'peer']
print(fruits[::-3]) # ['strawberry', 'apple'] - reverse